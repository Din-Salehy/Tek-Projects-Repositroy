package practice;

public class NameOfTheMonths {

	public static void main(String[] args) {
		
		byte month = 4;
		
		if (month ==1)
		{
			System.out.println("It is Janary");
		}
		
		else if(month == 2)
		{
			System.out.println("It is February");
		}
		
		else if(month == 3)
		{
			System.out.println("It is March");
		}
		
		else
		{
			System.out.println("Nothing");
		}
	}
}
